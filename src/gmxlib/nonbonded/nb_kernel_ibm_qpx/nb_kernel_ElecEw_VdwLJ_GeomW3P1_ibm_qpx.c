            fjx0             = vec_madd(dx00,fscal,fjx0);
            fjy0             = vec_madd(dy00,fscal,fjy0);
            fjz0             = vec_madd(dz00,fscal,fjz0);

            /**************************
             * CALCULATE INTERACTIONS *
             **************************/

            r10              = vec_mul(rsq10,rinv10);
            r10              = vec_sel(zero,r10,dummy_mask);

            /* Compute parameters for interactions between i and j atoms */
            qq10             = vec_mul(iq1,jq0);

            /* EWALD ELECTROSTATICS */

            /* Analytical PME correction */
            zeta2            = vec_mul(beta2,rsq10);
            rinv3            = vec_mul(rinvsq10,rinv10);
            pmecorrF         = gmx_ibm_qpx_pmecorrF(zeta2);
            felec            = vec_madd(pmecorrF,beta3,rinv3);
            felec            = vec_mul(qq10,felec);

            fscal            = felec;

            fscal            = vec_sel(zero,fscal,dummy_mask);

             /* Update vectorial force */
            fix1             = vec_madd(dx10,fscal,fix1);
            fiy1             = vec_madd(dy10,fscal,fiy1);
            fiz1             = vec_madd(dz10,fscal,fiz1);

            fjx0             = vec_madd(dx10,fscal,fjx0);
            fjy0             = vec_madd(dy10,fscal,fjy0);
