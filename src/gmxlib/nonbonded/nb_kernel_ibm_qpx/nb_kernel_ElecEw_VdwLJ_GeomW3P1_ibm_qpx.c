             /* Update vectorial force */
            fix2             = vec_madd(dx20,fscal,fix2);
            fiy2             = vec_madd(dy20,fscal,fiy2);
            fiz2             = vec_madd(dz20,fscal,fiz2);

            fjx0             = vec_madd(dx20,fscal,fjx0);
            fjy0             = vec_madd(dy20,fscal,fjy0);
            fjz0             = vec_madd(dz20,fscal,fjz0);

