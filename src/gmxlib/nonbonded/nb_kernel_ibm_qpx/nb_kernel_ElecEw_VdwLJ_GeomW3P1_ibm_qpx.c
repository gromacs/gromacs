            /* Analytical PME correction */
            zeta2            = vec_mul(beta2,rsq00);
            rinv3            = vec_mul(rinvsq00,rinv00);
            pmecorrF         = gmx_ibm_qpx_pmecorrF(zeta2);
            felec            = vec_madd(pmecorrF,beta3,rinv3);
            felec            = vec_mul(qq00,felec);

            /* LENNARD-JONES DISPERSION/REPULSION */

            rinvsix          = vec_mul(vec_mul(rinvsq00,rinvsq00),rinvsq00);
            fvdw             = vec_mul(vec_msub(c12_00,rinvsix,c6_00),vec_mul(rinvsix,rinvsq00));

            fscal            = vec_add(felec,fvdw);

            fscal            = vec_sel(zero,fscal,dummy_mask);

             /* Update vectorial force */
            fix0             = vec_madd(dx00,fscal,fix0);
            fiy0             = vec_madd(dy00,fscal,fiy0);
