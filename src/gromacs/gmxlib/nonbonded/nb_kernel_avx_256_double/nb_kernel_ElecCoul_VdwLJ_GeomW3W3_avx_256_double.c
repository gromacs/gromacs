            fjz1             = _mm256_add_pd(fjz1,tz);

            /**************************
             * CALCULATE INTERACTIONS *
             **************************/

            /* COULOMB ELECTROSTATICS */
            velec            = _mm256_mul_pd(qq02,rinv02);
            felec            = _mm256_mul_pd(velec,rinvsq02);

            fscal            = felec;

